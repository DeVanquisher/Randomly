package utils;

public class Utilities {
    private int classStrength;
    private int totCalledStudents;
    private String calledItem;
    private String filledItem;
    private String tryItem;
    private int calledItemIndex;

    public int getClassStrength() {
        return classStrength;
    }

    public void setClassStrength(int classStrength) {
        this.classStrength = classStrength;
    }

    public int getTotCalledStudents() {
        return totCalledStudents;
    }

    public void setTotCalledStudents(int totCalledStudents) {
        this.totCalledStudents = totCalledStudents;
    }

    public String getCalledItem() {
        return calledItem;
    }

    public void setCalledItem(String calledItem) {
        this.calledItem = calledItem;
    }

    public String getFilledItem() {
        return filledItem;
    }

    public void setFilledItem(String filledItem) {
        this.filledItem = filledItem;
    }

    public String getTryItem() {
        return tryItem;
    }

    public void setTryItem(String tryItem) {
        this.tryItem = tryItem;
    }

    public int getCalledItemIndex() {
        return calledItemIndex;
    }

    public void setCalledItemIndex(int calledItemIndex) {
        this.calledItemIndex = calledItemIndex;
    }
}
